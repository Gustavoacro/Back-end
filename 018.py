from math import sin, radians, cos, tan
valor = int(input("Digite o 창ngulo: "))
print(f"O 창ngulo de {valor} tem o SENO de {sin(radians(valor)):5.2f}")
print(f"O 창ngulo de {valor} tem o COSSENO de {cos(radians(valor)):5.2f}")
print(f"O 창ngulo de {valor} tem a TANGENTE de {tan(radians(valor)):5.2f}")